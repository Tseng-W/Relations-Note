import UIKit

extension UIFont {
  static func creamfont(size: CGFloat) -> UIFont {
    return UIFont(name: "creamfont-2.0", size: size)!
  }

  static func pingfang(size: CGFloat) -> UIFont {
    return UIFont(name: "PingFangTC-Regular", size: size)!
  }
}
